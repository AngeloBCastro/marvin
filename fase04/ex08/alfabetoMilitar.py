def alfabetoMilitar(vogal):
    if vogal in ["a", "A"]:
        return "alpha"
    if vogal in ["e", "E"]:
        return "echo"
    if vogal in ["i", "I"]:
        return "india"
    if vogal in ["o", "O"]:
        return "oscar"
    if vogal in ["u", "U"]:
        return "uniform"
    else:
        return false
